    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            // TODO add your handling code here:
            String name = jTextField3.getText();
            String email = jTextField1.getText();
            if(name.equals("")){
                showErr("Username cannot be empty");
                return;
            }
            if(email.equals("")){
                showErr("Email cannot be empty");
                return;
            }
            if(! (email.contains("@") && email.contains("."))){
                showErr("Email is not valid");
                return;
            }
            Client.u = new User(name, email);
            jButton1.setText("Connecting to Server...");
            //jButton1.setEnabled(false);
            try {
                
                Client.ss = new ServerSocket(0);
                sendConnectionDetails(Client.ss.getLocalPort(), Client.ss.getInetAddress().getHostName());
                Client.socket = Client.ss.accept();
            } catch (IOException ex) {
                Logger.getLogger(register_UI.class.getName()).log(Level.SEVERE, null, ex);
            }

            this.setVisible(false);
            Client.msgUi = new message_UI();
            Client.msgUi.setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(register_UI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

